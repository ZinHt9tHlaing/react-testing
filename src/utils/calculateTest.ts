export const sum = (x: number, y: number) => {
  return x + y;
};

export const subtract = (x: number, y: number) => {
  return x - y;
};

export const multiply = (x: number, y: number) => {
  return x * y;
};

export const divide = (x: number, y: number) => {
  return x / y;
};

export const square = (x: number) => {
  return x * x;
};

export const cube = (x: number) => {
  return x * x * x;
};